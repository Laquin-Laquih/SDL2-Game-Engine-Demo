Map::Map() :
	grass(TextureManager::load_texture("assets/grass.png")),
	dirt(TextureManager::load_texture("assets/dirt.png")),
	water(TextureManager::load_texture("assets/water.png"))
{
	load_map(level1);

	srcRect.x = 0;
	srcRect.y = 0;
	srcRect.w = 16;
	srcRect.h = 16;

	destRect.x = 0;
	destRect.y = 0;
	destRect.w = 32;
	destRect.h = 32;
}

Map::~Map()
{
}

template<size_t cols, size_t rows>
void Map::load_map(const std::array<std::array<int, cols>, rows>& arr)
{
	map = arr;
}

void Map::draw_map()
{
	Type type;
	for (int r = 0; r < map.size(); ++r)
	{
		for (int c = 0; c < map[0].size(); ++c)
		{
			type = (Type)map[r][c];

			destRect.x = c * destRect.w;
			destRect.y = r * destRect.h;
			switch (type)
			{
				case GRASS:
					TextureManager::draw(*grass, srcRect, destRect);
					break;
				case DIRT:
					TextureManager::draw(*dirt, srcRect, destRect);
					break;
				case WATER:
					TextureManager::draw(*water, srcRect, destRect);
					break;
			}
		}
	}
}
